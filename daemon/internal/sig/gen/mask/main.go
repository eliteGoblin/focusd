//go:build ignore

// Generator: produces daemon/internal/sig/pubkey_masked.go by XOR-masking
// the offline Ed25519 PEM with a deterministic 32-byte mask. Runs only via
// `go generate ./daemon/internal/sig`; never linked into the daemon
// binary itself.
//
// HONEST SCOPE: the mask is a public, deterministic value derived from a
// label split across two string literals so a single
// `strings | grep "focusd-pubkey-mask-v1"` against the binary fails. It
// does NOT defeat a debugger or disassembler — an attacker who locates
// the decode routine can recover the PEM trivially. The goal is to raise
// the bar from "trivial grep" to "load in a disassembler", which is
// enough friction to deter a half-hearted weak moment.
package main

import (
	"bytes"
	"crypto/sha256"
	"fmt"
	"go/format"
	"log"
	"os"
	"path/filepath"
	"runtime"
)

func main() {
	// Source PEM lives next to the generated file (../../ relative to this
	// main.go: daemon/internal/sig/gen/mask -> daemon/internal/sig).
	_, thisFile, _, ok := runtime.Caller(0)
	if !ok {
		log.Fatal("runtime.Caller failed")
	}
	sigDir := filepath.Clean(filepath.Join(filepath.Dir(thisFile), "..", ".."))
	pemPath := filepath.Join(sigDir, "focusd_ed25519_public.pem")
	outPath := filepath.Join(sigDir, "pubkey_masked.go")

	pem, err := os.ReadFile(pemPath)
	if err != nil {
		log.Fatalf("read PEM: %v", err)
	}

	mask := deriveMask()
	masked := xorMask(pem, mask)

	src, err := renderSource(masked)
	if err != nil {
		log.Fatalf("format generated source: %v", err)
	}

	if err := os.WriteFile(outPath, src, 0o644); err != nil {
		log.Fatalf("write %s: %v", outPath, err)
	}
	fmt.Printf("wrote %s (%d masked bytes)\n", outPath, len(masked))
}

// deriveMask returns the deterministic 32-byte XOR mask. The label is
// split across two literals so `strings | grep "focusd-pubkey-mask-v1"`
// against the daemon binary does not find a contiguous match.
func deriveMask() [32]byte {
	const labelA = "focusd-pubkey-"
	const labelB = "mask-v1"
	return sha256.Sum256([]byte(labelA + labelB))
}

func xorMask(src []byte, mask [32]byte) []byte {
	out := make([]byte, len(src))
	for i, b := range src {
		out[i] = b ^ mask[i%len(mask)]
	}
	return out
}

// renderSource emits a gofmt-clean Go file containing the masked bytes
// as a package-level []byte literal. Deterministic: same input PEM ->
// byte-identical output.
func renderSource(masked []byte) ([]byte, error) {
	var buf bytes.Buffer
	buf.WriteString("// Code generated by `go run ./gen/mask`. DO NOT EDIT.\n")
	buf.WriteString("package sig\n\n")
	buf.WriteString("// maskedPubkey is the offline Ed25519 PEM XOR-masked with a\n")
	buf.WriteString("// deterministic 32-byte key derived at runtime in pubkey.go.\n")
	buf.WriteString("// Regenerate via `go generate ./daemon/internal/sig` if the PEM rotates.\n")
	buf.WriteString("var maskedPubkey = []byte{\n")
	const perLine = 8
	for i, b := range masked {
		if i%perLine == 0 {
			buf.WriteString("\t")
		}
		fmt.Fprintf(&buf, "0x%02x,", b)
		if (i+1)%perLine == 0 || i == len(masked)-1 {
			buf.WriteString("\n")
		} else {
			buf.WriteString(" ")
		}
	}
	buf.WriteString("}\n")
	return format.Source(buf.Bytes())
}
